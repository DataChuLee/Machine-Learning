# 출산 예측 모델 개발 
## 목적: 출산을 예측하여 예비 부모에게 미리 유아 용품 프로모션 제안하여 고객 선점
## 현황: 공개된 출생 기록을 기반으로 부모에게 프로모션 제안
## 출산 전 고객 패턴: 식재료, 옷장, 비타민 종류 등을 변경하는 경향이 있음.
## 필요 데이터: 출산한 고객의 구매 시점, 상품명이 포함되는 구매 데이터(영수증 데이터)

# 데이터 분석가에게 필요한 역량 
## Domain Knowledge - 가장 중요
### Domain Knowledge를 통해 출산 전 고객패턴을 이해할 수 있었고, 이를 모델링 할 수 있었음.

## 통계학적 지식 - 현상을 통계적으로 좀 더 정확하게 바라보기 위해 
### 데이터셋을 구성하고 통계(머신러닝)모델을 만드는데 사용

## 커뮤니케이션 능력 - 분석한 내용을 남들에게 전달/설득하는데 필요 
### 출생 예측 모델을 만든 후 예측값을 기반으로 프로모션을 진행할 수 있도록 논리적인 설득 

